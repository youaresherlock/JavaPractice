package foundation;

public class StringPlus {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        // 字符串可以和数字直接相加
        System.out.println("a == b = " + (a == b) );
        System.out.println("a != b = " + (a != b) );
        System.out.println("a > b = " + (a > b) );
        System.out.println("a < b = " + (a < b) );
        System.out.println("b >= a = " + (b >= a) );
        System.out.println("b <= a = " + (b <= a) );
    }
}
